#include <stdio.h>
#include <stdlib.h>

int main()
{
    int i,j;
    for(i=1;i<10;i++)
    {
        printf("-------------------------------------------------------------------------\n");
        for(j=1;j<10;j++)
        {
            printf("%d\t",(i*j));
            printf("|  ");
        }
        printf("\n");
    }
    printf("-------------------------------------------------------------------------");
    return 0;
}
